"""## 13. Threshold Tuning

For medical datasets, threshold tuning can improve recall/F1/accuracy depending on the goal. Here the threshold that maximizes validation F1-score is selected.
"""

def tune_threshold(y_true, y_proba, metric="f1"):
    thresholds = np.arange(0.05, 0.96, 0.01)
    rows = []
    for t in thresholds:
        pred = (y_proba >= t).astype(int)
        rows.append({
            "threshold": t,
            "accuracy": accuracy_score(y_true, pred),
            "precision": precision_score(y_true, pred, zero_division=0),
            "recall": recall_score(y_true, pred, zero_division=0),
            "f1": f1_score(y_true, pred, zero_division=0),
        })
    table = pd.DataFrame(rows)
    best_row = table.loc[table[metric].idxmax()]
    return float(best_row["threshold"]), table

