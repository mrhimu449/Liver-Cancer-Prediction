"""## 8. Helper Functions"""

def build_pipeline(model, use_smote=False, add_features=True):
    steps = [
        ("feature_engineering", ClinicalFeatureEngineer(add_features=add_features)),
        ("preprocess", preprocessor),
    ]
    if use_smote:
        steps.append(("smote", SMOTE(random_state=RANDOM_STATE)))
    steps.append(("model", model))
    return ImbPipeline(steps=steps)


def evaluate_predictions(y_true, y_pred, y_proba=None, label="Model"):
    metrics = {
        "model": label,
        "accuracy": accuracy_score(y_true, y_pred),
        "precision": precision_score(y_true, y_pred, zero_division=0),
        "recall": recall_score(y_true, y_pred, zero_division=0),
        "f1": f1_score(y_true, y_pred, zero_division=0),
    }
    if y_proba is not None:
        metrics["roc_auc"] = roc_auc_score(y_true, y_proba)
    return metrics


def show_confusion_matrix(y_true, y_pred, title="Confusion Matrix"):
    cm = confusion_matrix(y_true, y_pred)
    plt.figure(figsize=(5, 4))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues")
    plt.title(title)
    plt.xlabel("Predicted")
    plt.ylabel("Actual")
    plt.show()


def get_positive_proba(model, X_data):
    if hasattr(model, "predict_proba"):
        return model.predict_proba(X_data)[:, 1]
    if hasattr(model, "decision_function"):
        scores = model.decision_function(X_data)
        return (scores - scores.min()) / (scores.max() - scores.min() + 1e-9)
    return None


def summarize_cv_results(cv_result, model_name):
    row = {"model": model_name}
    for key, values in cv_result.items():
        if key.startswith("test_"):
            metric = key.replace("test_", "")
            row[f"{metric}_mean"] = np.mean(values)
            row[f"{metric}_std"] = np.std(values)
    return row

