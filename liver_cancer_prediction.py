"""## 6. Leakage-Safe Clinical Feature Engineering

"""

class ClinicalFeatureEngineer(BaseEstimator, TransformerMixin):
    def __init__(self, add_features=True):
        self.add_features = add_features

    def fit(self, X, y=None):
        return self

    def transform(self, X):
        X = X.copy()
        if not self.add_features:
            return X

        if "bmi" in X.columns:
            X["bmi_log"] = np.log1p(np.clip(X["bmi"].astype(float), a_min=0, a_max=None))

        if {"hepatitis_b", "hepatitis_c"}.issubset(X.columns):
            X["viral_hepatitis_any"] = ((X["hepatitis_b"] == 1) | (X["hepatitis_c"] == 1)).astype(int)

        if {"age", "bmi"}.issubset(X.columns):
            X["age_bmi_interaction"] = X["age"].astype(float) * X["bmi"].astype(float)

        risk_cols = [
            c for c in [
                "hepatitis_b", "hepatitis_c", "cirrhosis_history",
                "family_history_cancer", "diabetes"
            ] if c in X.columns
        ]
        if risk_cols:
            X["clinical_risk_score"] = X[risk_cols].sum(axis=1)

        if {"alpha_fetoprotein_level", "liver_function_score"}.issubset(X.columns):
            X["afp_liver_function_ratio"] = (
                X["alpha_fetoprotein_level"].astype(float) /
                (X["liver_function_score"].astype(float) + 1e-6)
            )

        return X

